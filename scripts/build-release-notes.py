#!/usr/bin/env python3
"""Generate docs/RELEASE_NOTES.md — a machine-generated release snapshot.

docs/RELEASE.md asks maintainers to hand-assemble catalog stats, eval counts,
and quality-gate results at release time. This generator replaces that manual
step: it derives every number from the committed metadata (catalog, provenance,
eval-harness scenarios, benchmark tasks, tools catalog, rigor coverage), so the
snapshot is reproducible from a checkout and can never drift from the artifacts
it describes. Maintainers paste the snapshot into the GitHub release and add a
hand-written Highlights section on top.

Zero third-party dependencies (TOML via scripts/toml_compat.py). Mirrors the
build-*/--check pattern of the other generators.

With --html, also writes docs/releases/index.html — a single-file static page
that mirrors the same snapshot for the GitHub Pages site (so users browsing
the catalog don't need to open the .md). The HTML reuses the CSS variables
and layout tokens of docs/search.html so the two pages look like siblings.
"""

from __future__ import annotations

import argparse
import html
import importlib.util
import json
import sys
from pathlib import Path

import toml_compat

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT / "eval-harness" / "lib"))
from checks import AUTO_CHECKS  # noqa: E402


def _load_coverage_map():
    """The coverage map owns the method-family roster (METHOD_ORDER); reuse it
    so the snapshot and badge can never disagree with docs/RIGOR_COVERAGE.md."""
    path = ROOT / "scripts" / "build-coverage-map.py"
    spec = importlib.util.spec_from_file_location("aers_build_coverage_map", path)
    module = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(module)
    return module


COVERAGE_MAP = _load_coverage_map()
CATALOG = ROOT / "catalog" / "skills.json"
PROVENANCE = ROOT / "catalog" / "provenance.json"
ENRICHED = ROOT / "catalog" / "skills-enriched.json"
TOOLS = ROOT / "tools" / "tools.json"
SCENARIO_DIR = ROOT / "eval-harness" / "scenarios"
TASK_DIR = ROOT / "benchmark" / "tasks"
OUT = ROOT / "docs" / "RELEASE_NOTES.md"
BADGE_OUT = ROOT / "docs" / "badges" / "rigor-coverage.json"
HTML_OUT = ROOT / "docs" / "releases" / "index.html"


def _repo_slug() -> str:
    """Best-effort GitHub owner/repo slug for the HTML 'GitHub releases' link.

    Falls back to a hardcoded default when `git` is unavailable so the page
    still renders. Maintained by hand in lockstep with the repo URL.
    """
    try:
        import subprocess
        out = subprocess.check_output(
            ["git", "remote", "get-url", "origin"],
            cwd=str(ROOT),
            stderr=subprocess.DEVNULL,
            text=True,
        ).strip()
    except Exception:
        return "brycewang-stanford/Auto-Empirical-Research-Skills"
    s = out
    for prefix in ("git@github.com:", "https://github.com/", "ssh://git@github.com/"):
        if s.startswith(prefix):
            s = s[len(prefix):]
            break
    if s.endswith(".git"):
        s = s[:-4]
    return s or "brycewang-stanford/Auto-Empirical-Research-Skills"


def load_json(path: Path) -> dict:
    with path.open(encoding="utf-8") as fh:
        return json.load(fh)


def scenario_stats() -> dict:
    n_scenarios = 0
    n_rubric = 0
    n_auto = 0
    categories: dict[str, int] = {}
    for path in sorted(SCENARIO_DIR.glob("*.toml")):
        with path.open("rb") as fh:
            s = toml_compat.load(fh)
        n_scenarios += 1
        categories[s.get("category", "?")] = categories.get(s.get("category", "?"), 0) + 1
        for item in s.get("rubric", []):
            n_rubric += 1
            if item.get("check") in AUTO_CHECKS:
                n_auto += 1
    return {
        "scenarios": n_scenarios,
        "rubric_items": n_rubric,
        "auto_checks": n_auto,
        "categories": categories,
    }


def benchmark_stats() -> dict:
    n_tasks = 0
    n_gold = 0
    n_required = 0
    for path in sorted(TASK_DIR.glob("*.toml")):
        with path.open("rb") as fh:
            t = toml_compat.load(fh)
        n_tasks += 1
        for g in t.get("gold", []):
            n_gold += 1
            if g.get("required"):
                n_required += 1
    return {"tasks": n_tasks, "gold_checks": n_gold, "required_checks": n_required}


def build() -> str:
    catalog = load_json(CATALOG)
    provenance = load_json(PROVENANCE)
    enriched = load_json(ENRICHED)
    tools = load_json(TOOLS)
    scen = scenario_stats()
    bench = benchmark_stats()

    summary = catalog.get("summary", {})
    methods = enriched.get("taxonomy", {}).get("method", {})
    licenses = provenance.get("summary", {}).get("licenses", {})

    lines = [
        "# Release Snapshot",
        "",
        "Generated by `scripts/build-release-notes.py` from committed metadata; do not edit by hand.",
        "Regenerate with `make catalog`. Freshness is enforced by `make validate`.",
        "",
        "Paste this snapshot into the GitHub release body and add a hand-written",
        "**Highlights** section on top (see [`RELEASE.md`](RELEASE.md)).",
        "",
        "## Catalog",
        "",
        f"- Top-level collections: **{summary.get('top_level_collections', '?')}**",
        f"- `SKILL.md` files: **{summary.get('skill_files', '?')}**",
        f"- Cataloged agent tools: **{len(tools.get('tools', []))}** ([`tools/CATALOG.md`](../tools/CATALOG.md))",
        "",
        "## Methodological rigor",
        "",
        f"- Method families in the coverage map: **{len(COVERAGE_MAP.METHOD_ORDER)}** ([`RIGOR_COVERAGE.md`](RIGOR_COVERAGE.md))",
        f"- Eval scenarios: **{scen['scenarios']}** "
        f"({scen['rubric_items']} rubric items, {scen['auto_checks']} auto-checkable) "
        "([`../eval-harness/`](../eval-harness/README.md))",
        f"- Benchmark tasks: **{bench['tasks']}** "
        f"({bench['gold_checks']} gold checks, {bench['required_checks']} required) "
        "([`../benchmark/`](../benchmark/README.md))",
        "",
        "### Method families (skills tagged)",
        "",
    ]
    for tag in sorted(methods, key=lambda k: (-methods[k], k)):
        lines.append(f"- `{tag}`: {methods[tag]}")
    lines += [
        "",
        "### Eval scenarios by category",
        "",
    ]
    for cat in sorted(scen["categories"]):
        lines.append(f"- `{cat}`: {scen['categories'][cat]}")
    lines += [
        "",
        "## License buckets",
        "",
    ]
    for name in sorted(licenses):
        lines.append(f"- {name}: {licenses[name]}")
    lines += [
        "",
        "## Quality gates",
        "",
        "- `make check` — catalog/provenance/audit/eval freshness, repo hygiene,",
        "  Paper-WorkFlow gates, stdlib unit tests, eval-harness lint + graded smoke,",
        "  and the strict numeric benchmark (reference-gold drift fails the build).",
        "- External links and OpenSSF Scorecard run on their scheduled workflows.",
        "",
    ]
    return "\n".join(lines)


def build_badge() -> str:
    """Shields.io endpoint JSON summarizing the rigor surface (README badge)."""
    scen = scenario_stats()
    bench = benchmark_stats()
    payload = {
        "schemaVersion": 1,
        "label": "rigor",
        "message": (
            f"{len(COVERAGE_MAP.METHOD_ORDER)} method families · "
            f"{bench['tasks']} benchmarks · {scen['scenarios']} evals"
        ),
        "color": "brightgreen",
    }
    return json.dumps(payload, indent=2) + "\n"


# ---------------------------------------------------------------------------
# Markdown -> HTML for the GitHub-Pages release page
# ---------------------------------------------------------------------------
#
# We deliberately don't pull in a markdown library: the RELEASE_NOTES.md file
# has a small, well-defined grammar (H1/H2/H3 headings + bullets + inline code,
# bold, and links) and we already control its emission from `build()`. A 60-line
# parser keeps the script stdlib-only and avoids `pip install` drift.


def _md_inline(text: str) -> str:
    """Render inline markdown (`code`, **bold**, [link](href)).

    Order matters: escape HTML first so user-generated identifiers can't
    inject tags, then apply the inline substitutions. Backticks are matched
    greedily by splitting on them.
    """
    out = html.escape(text, quote=False)

    # Inline code: `code` -> <code>code</code>. We split on backticks so any
    # user-supplied content between them keeps its inner HTML-escaped form.
    if "`" in out:
        parts = out.split("`")
        rebuilt = []
        in_code = False
        for piece in parts:
            if in_code:
                rebuilt.append(f"<code>{piece}</code>")
            else:
                rebuilt.append(piece)
            in_code = not in_code
        out = "".join(rebuilt)

    # Bold: **text** -> <strong>text</strong>. Run AFTER code so backticked
    # content with literal asterisks isn't double-processed.
    import re

    out = re.sub(r"\*\*([^*]+)\*\*", r"<strong>\1</strong>", out)

    # Links: [text](href). We accept both relative paths and absolute URLs.
    out = re.sub(
        r"\[([^\]]+)\]\(([^)]+)\)",
        lambda m: f'<a href="{html.escape(m.group(2), quote=True)}">{m.group(1)}</a>',
        out,
    )
    return out


def _md_body_to_html(md: str) -> str:
    """Convert the RELEASE_NOTES.md body to HTML, split into H2 sections.

    Skips the top-of-file H1 (`# Release Snapshot`) and the 5-line preamble
    block — those move into the page header. Each subsequent `## Heading`
    becomes a `<section>` with its own anchor id derived from the heading
    text. The page is rendered top-down with newest first, so the same
    section ordering as the markdown.
    """
    lines = md.split("\n")
    # Drop the leading H1 line.
    while lines and not lines[0].startswith("# "):
        lines.pop(0)
    if lines and lines[0].startswith("# "):
        lines.pop(0)
    # Drop preamble until the first H2.
    while lines and not lines[0].startswith("## "):
        lines.pop(0)

    sections: list[tuple[str, list[str]]] = []
    current_title: str | None = None
    current_body: list[str] = []

    def _flush() -> None:
        if current_title is not None:
            sections.append((current_title, current_body))
        else:
            # Anything collected before the first H2 goes into a "Preview"
            # bucket so it isn't silently dropped.
            if current_body:
                sections.append(("Preview", current_body))

    for raw in lines:
        line = raw.rstrip()
        if line.startswith("## "):
            _flush()
            current_title = line[3:].strip()
            current_body = []
        else:
            current_body.append(line)
    _flush()

    parts: list[str] = []
    for title, body in sections:
        slug = html.escape(
            "".join(ch.lower() if ch.isalnum() else "-" for ch in title).strip("-")
            or "section"
        )
        parts.append(f'<section class="release-section" id="sec-{slug}">')
        parts.append(f"<h2>{html.escape(title)}</h2>")
        # Walk the body lines and emit small HTML chunks. We keep the same
        # bullet-grouping semantics as CommonMark: a blank line separates
        # paragraphs from lists.
        i = 0
        while i < len(body):
            line = body[i]
            stripped = line.strip()
            if not stripped:
                i += 1
                continue
            if stripped.startswith("- "):
                # Collect a contiguous run of bullets, including CommonMark
                # "lazy continuation" lines (lines that are indented but
                # don't start with `- `).
                items: list[str] = []
                while i < len(body):
                    raw_line = body[i]
                    line_strip = raw_line.strip()
                    if line_strip.startswith("- "):
                        items.append(line_strip[2:])
                        i += 1
                    elif line_strip == "":
                        # Blank line ends the list.
                        break
                    elif line_strip.startswith(("- ", "### ")) or line_strip.startswith("#"):
                        break
                    else:
                        # Treat as a continuation line for the previous bullet.
                        if items:
                            items[-1] = items[-1] + " " + line_strip
                        i += 1
                parts.append("<ul>")
                for item in items:
                    parts.append(f"<li>{_md_inline(item)}</li>")
                parts.append("</ul>")
                continue
            if stripped.startswith("### "):
                parts.append(f"<h3>{html.escape(stripped[4:])}</h3>")
                i += 1
                continue
            # Plain paragraph: take contiguous non-empty lines until the next
            # blank line or list/heading.
            para: list[str] = [stripped]
            i += 1
            while i < len(body) and body[i].strip() and not body[i].strip().startswith(("- ", "### ")):
                para.append(body[i].strip())
                i += 1
            parts.append(f"<p>{_md_inline(' '.join(para))}</p>")
        parts.append("</section>")
    return "\n".join(parts), sections


def _stats_summary_html() -> str:
    """Top-of-page at-a-glance card row derived from the same stats as the markdown."""
    catalog = load_json(CATALOG)
    enriched = load_json(ENRICHED)
    tools = load_json(TOOLS)
    scen = scenario_stats()
    bench = benchmark_stats()
    summary = catalog.get("summary", {})
    methods = enriched.get("taxonomy", {}).get("method", {})

    cards = [
        ("Collections", summary.get("top_level_collections", "?"), "Top-level skill folders"),
        ("Skills", summary.get("skill_files", "?"), "SKILL.md entries"),
        ("Method families", len(COVERAGE_MAP.METHOD_ORDER), "Distinct causal-inference families"),
        ("Eval scenarios", scen["scenarios"], f"{scen['auto_checks']} auto-checkable rubric items"),
        ("Benchmark tasks", bench["tasks"], f"{bench['required_checks']} required gold checks"),
        ("Tools", len(tools.get("tools", [])), "Agent tools cataloged"),
        ("Method-skills", sum(methods.values()), "Skills tagged with a method family"),
    ]
    out = ['<section class="stat-grid" aria-label="Snapshot at a glance">']
    for label, value, hint in cards:
        out.append(
            '<div class="stat-card">'
            f'<div class="stat-value">{html.escape(str(value))}</div>'
            f'<div class="stat-label">{html.escape(label)}</div>'
            f'<div class="stat-hint">{html.escape(hint)}</div>'
            "</div>"
        )
    out.append("</section>")
    return "\n".join(out)


